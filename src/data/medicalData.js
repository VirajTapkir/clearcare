export function getDiagnosis(selectedSymptoms) {
  if (selectedSymptoms.length === 0) return null;

  const scores = diagnoses.map(d => {
    const matches = d.symptoms.filter(s => selectedSymptoms.includes(s)).length;
    if (matches === 0) return null;
    const coverage = matches / d.symptoms.length;
    const symptomCoverage = matches / selectedSymptoms.length;
    return {
      diagnosis: d,
      score: (coverage * 0.6 + symptomCoverage * 0.4) * matches,
      matches,
    };
  }).filter(Boolean);

  scores.sort((a, b) => b.score - a.score);
  return scores.slice(0, 3);
}

export function getHospitalCost(hospital, diagnosis) {
  const estimated = Math.round(diagnosis.avgTotalCost * hospital.costMultiplier);
  const outOfPocket = Math.round(diagnosis.avgOutOfPocket * hospital.costMultiplier);
  return { estimated, outOfPocket };
}
